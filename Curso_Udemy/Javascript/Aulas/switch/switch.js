

let parametro = prompt('Digite numero')

switch (parseInt(parametro)) {
    case 1:
        document.write('Paremetro 1')
        break

    case 2:
        document.write('Paremetro 2')
        break

    case 3:
        document.write('Paremetro 3')
        break

    default:
        document.write('Default')
        break;
}